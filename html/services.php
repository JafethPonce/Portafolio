        <h2 class="title-section">concepto y caracteristicas de las 
                    aplicaciones web hibridas(Mashup)<span class="marked">
                </span> SOA</h2>
                
                 <u><font face="Arial" size="5">Concepto:</font></u>
                 <p><font face="Arial" size="3" color = "black">Una Mashup o aplicación web híbrida es una aplicación que combina servicios de varias páginas web en una sola para ofrecer un nuevo servicio.
                     Es habitual que la integración de datos se obtenga desde APIs abiertas.
                     Las aplicaciones híbridas son populares porque permiten a los desarrolladores escribir código para una aplicación móvil una vez y aún se adaptan a múltiples plataformas. Debido a que las aplicaciones híbridas agregan una capa adicional entre el código fuente y la plataforma de destino,
                    pueden funcionar un poco más lento que las versiones nativas o web de la misma aplicación.</font></p>
                <u><font face= "Arial" size="5">Características de las aplicaciones híbridas:</font></u>
                <p><font face="Arial" size="3" color = "black">Las características de las aplicaciones híbridas incluyen:</font></p>
                <ul>
                    <li>
                    <font face="Arial" size="3" color = "black">La capacidad de funcionar esté o no conectado el dispositivo.</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">Integración con el sistema de archivos del dispositivo móvil.</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">Integración con servicios basados ​​en web.</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">Un navegador integrado para mejorar el acceso a contenido dinámico en línea.</font>
                    </li>
                </ul>
                <u><font face= "Arial" size="5">Cómo funcionan las aplicaciones híbridas</font></u>
                <p><font face="Arial" size="3" color = "black"> Las aplicaciones híbridas funcionan de manera similar a las aplicaciones web pero, al igual que las aplicaciones nativas, se descargan en el dispositivo. Al igual que las aplicaciones web, las aplicaciones híbridas suelen estar escritas en HTML5, CSS y JavaScript. Las aplicaciones híbridas ejecutan código dentro de un contenedor. El motor del navegador del dispositivo se utiliza para representar HTML y JavaScript y API nativas para acceder al hardware específico del dispositivo.

Aunque una aplicación híbrida normalmente compartirá elementos de navegación similares a los de una aplicación web, el hecho de que una aplicación funcione sin conexión depende de sus funcionalidades. Si una aplicación no necesita soporte de una base de datos, entonces se puede hacer que funcione sin conexión.</font></p>

<h2 class="title-section">servicios en la nube<span class="marked">
                </span> SOA</h2>
                <u><font face="Arial" size="5">¿Qué es un servicio de nube?</font></u>
                <p><font face="Arial" size="3" color = "black">Los servicios de nube son infraestructuras, plataformas o sistemas de software que alojan los proveedores externos y que se ponen a disposición 
                    de los usuarios a través de Internet. <br>
                    Facilitan el flujo de datos de los usuarios a través de Internet, desde los clientes frontend (p. ej., los servidores, las tabletas y las computadoras portátiles o de escritorio; es decir, cualquier sistema en el extremo del usuario) hasta los sistemas de los proveedores, y viceversa. Para acceder a los servicios de nube, los usuarios solo necesitan una computadora, un sistema operativo 
                    y conexión a Internet o una red privada virtual (VPN).
                </font></p>
                <u><font face="Arial" size="5">Tipos de servicios de nube</font></u>
                <p><font face="Arial" size="3" color = "black">Los servicios de cloud computing son todas las infraestructuras, las plataformas, las tecnologías o los sistemas de software a los que acceden los usuarios a través de Internet sin tener que descargar software adicional, al igual que las 
                    soluciones como servicio que se mencionan a continuación.
                </font></p>
                <ul>
                    <li>
                    <font face="Arial" size="3" color = "black">La Infraestructura como servicio (IaaS) pone recursos informáticos,
                         de red y de almacenamiento a disposición de los usuarios.</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">La Plataforma como servicio (PaaS) ofrece una plataforma en la que se pueden ejecutar las aplicaciones, 
                        así como toda la infraestructura de TI que se necesita para que funcione.</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">El Software como servicio (SaaS) ofrece a los usuarios una aplicación 
                        en la nube, la plataforma en la que se ejecuta y la infraestructura subyacente.</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">La Función como servicio (FaaS) es un modelo de ejecución basado en eventos que permite que los desarrolladores diseñen, ejecuten y gestionen paquetes de 
                        aplicaciones como funciones, sin tener que preocuparse por el mantenimiento de la infraestructura.</font>
                    </li>
                </ul>
                <u><font face="Arial" size="5">Funcionamiento de los servicios de nube</font></u>
                <p><font face="Arial" size="3" color = "black">Al igual que todas las otras soluciones de TI, los servicios de nube dependen de los sistemas de hardware y software. Sin embargo, se diferencian en que los usuarios
                     solo necesitan una computadora, una conexión a la red y un sistema operativo para acceder a ellos.
                </font></p>
                <u><font face="Arial" size="5">Infraestructura de nube</font></u>
                <p><font face="Arial" size="3" color = "black">Cuando los proveedores ponen una infraestructura de nube a disposición de los usuarios, 
                    separan las funciones informáticas de los elementos de hardware, como en los siguientes casos:
                </font></p>
                <ul>
                    <li>
                    <font face="Arial" size="3" color = "black">La potencia de procesamiento de las unidades centrales de procesamiento (CPU)</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">La memoria activa de los chips de memoria de acceso aleatorio (RAM)</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">El procesamiento de gráficos de las unidades de procesamiento de gráficos (GPU)</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">La disponibilidad de almacenamiento de datos de los centros de datos o discos duros</font>
                    </li>
                </ul>
                <p><font face="Arial" size="3" color = "black">Por lo general, esta abstracción se logra a través de la virtualización y las máquinas virtuales. Una vez separados, los usuarios obtienen los elementos informáticos, de almacenamiento y de red a través del Internet como servicio (IaaS). Este tipo de servicio ha generado un aumento en el almacenamiento en la nube, donde se aloja el big data como parte del Internet de las cosas (IoT).
                     La empresa RackSpace es un ejemplo de un proveedor de IaaS.
                </font></p>
                <u><font face="Arial" size="5">Plataformas de nube</font></u>
                <p><font face="Arial" size="3" color = "black">Los proveedores de servicios de nube también pueden utilizar sus recursos de hardware para crear plataformas de nube: entornos en línea donde los usuarios desarrollan código o ejecutan aplicaciones. Diseñarlas implica mucho más que extraer las funciones informáticas de los elementos de hardware, como sucede cuando se ofrece una infraestructura de nube. Se requiere un nivel de desarrollo superior para incorporar tecnologías como la organización en contenedores, la coordinación, las interfaces de programación de aplicaciones (API), el enrutamiento, la seguridad, la gestión y la automatización. También se debe tener en cuenta el diseño de la experiencia del usuario (UX) a la hora de crear una experiencia en línea accesible.

Las plataformas de nube son un tipo de PaaS que pueden considerarse nubes siempre y cuando los elementos de la infraestructura que las mantienen se puedan adaptar y compartir con facilidad. Los mejores ejemplos de nubes de PaaS son las nubes privadas gestionadas y las públicas.
                </font></p>
                <ul>
                    <li>
                    <font face="Arial" size="3" color = "black">Proveedores de nube pública
Separan sus propias infraestructuras, plataformas o aplicaciones de los sistemas de hardware que poseen; las agrupan en lagos de datos; y las comparten con varios usuarios. También pueden ofrecer servicios de nube pública, como la gestión de las API, los sistemas operativos basados en la nube o las bibliotecas de plantillas de desarrollo (conocidas como marcos). Algunas de las nubes públicas más conocidas son Alibaba Cloud, Microsoft Azure, Google Cloud, IBM Cloud y Amazon Web Services (AWS).</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">Nubes privadas gestionadas
Ofrecen a los clientes una nube privada cuya implementación, configuración y gestión está a cargo de un tercero. Esta opción permite que las empresas o los negocios pequeños con equipos de TI sin personal suficiente o sin las habilidades necesarias presten mejores servicios de nube privada e infraestructura a los usuarios.</font>
                    </li>
                </ul>
                <u><font face="Arial" size="5">Software de nube</font></u>
                <p><font face="Arial" size="3" color = "black">El último servicio de nube ampliamente aceptado que pueden prestar los proveedores es una aplicación web completa, conocida como software de nube o SaaS. Para ello, se requiere la mayor inversión en desarrollo, ya que se ofrece literalmente una aplicación en línea a los clientes.

El software de nube se puede ofrecer con un enfoque basado en la nube, es decir, como una arquitectura de aplicación que combina microservicios pequeños, independientes y sin conexión directa. Se pueden agrupar varios microservicios en contenedores Linux® individuales cuya gestión está a cargo de un motor de organización de contenedores, como Kubernetes o Red Hat® OpenShift®. El producto final es una aplicación de nube conformada por varios microservicios que se puede mejorar por medio de cada uno de ellos de manera individual, sin afectar al resto.

Otro lenguaje de programación muy conocido para las aplicaciones en línea, las aplicaciones móviles y los productos de SaaS es el Hyper Text Markup Language 5 (HTML5). Incorpora tres lenguajes de programación para crear aplicaciones a las que se pueda acceder desde un explorador web:

HTML: agrega contenido a las páginas web.
Cascading Style Sheets (CSS): agrega elementos de presentación al contenido de las páginas web.
JavaScript: agrega elementos interactivos al contenido de las páginas web.
                </font></p>
                <h2 class="title-section">caracteristicas de las aplicaciones orientadas a servicios<span class="marked">
                </span> SOA</h2>
                <p><font face="Arial" size="3" color = "black"> <u>Proveedor de servicios <br></u> 
Se encarga de crear servicios web, ofrecerlos a un registro de servicios disponibles y gestionar sus condiciones de uso <br>

<u> Agente o registro de servicios </u> <br>
Se encarga de brindar información acerca del servicio a quien lo solicite, y puede ser público o privado. <br>

<u> Usuario del servicio o persona que lo solicita <br></u>
Buscará un servicio en el registro o por medio del agente, y se conectará con el proveedor para recibirlo.
                </font></p>
                <strong><p><font face = "Arial" size = "10" color = "black">
                    Arquitectura orientada a servicios(SOA)
                </font></p></strong>
                <h2 class="title-section">
                    Definir la arquitectura orientada a servicios
                <span class="marked">
                </span> SOA</h2>
                <p><font face="Arial" size="3" color = "black">La arquitectura orientada a los servicios (SOA) es un tipo de diseño de software que permite reutilizar sus elementos gracias a las interfaces de servicios que se comunican a través de una red con un lenguaje común
                </font></p>
                <h2><font color = "black">
                <span class="marked">
                    Identificar los principios de diseño que se aplican a sobre cada servicio modelado
                </span> SOA </font></h2>
                <img src="../assets/img/111.png" alt="" width = "50%">

                <p><font face="Arial" size="3" color = "black">

Los ocho principios de diseño de SOA.

Son estos: <br>

Contratos estandarizados: los servicios dentro del mismo inventario, deben estar acordes a los mismos principios de diseño de contratos. <br>

Bajo acoplamiento: de los servicios respecto a su entorno y, a su vez, imponer bajo acoplamiento a sus consumidores. <br>

Abstracción: con unos contratos que sólo contienen lo esencial y sin que exista más información que la publicada en los contratos. <br>

Reutilización: contenedores de lógica agnóstica que pueda ser fácilmente reutilizada en diferentes contextos. <br>

Autonomía: ejerciendo un alto control sobre el entorno de ejecución subyacente. <br>

Ausencia de estado <br>

Capacidad de ser descubiertos: complementados con metadatos que hacen que puedan ser adecuadamente descubiertos e interpretados. <br>

Capacidad de ser combinados: formando lógicas más complejas. <br>

Los Servicios Web se basan en estándares y protocolos abiertos. A continuación se describen los estándares de forma breve: <br>

XML (Extensible Markup Language): Es el formato estándar para los datos que se vayan a intercambiar; basado en marcas y etiquetas, es muy frecuente el uso de este metalenguaje para crear otros lenguajes con entidad propia. Su simpleza, facilita su uso fundamentalmente en el intercambio de una gran variedad de datos. <br>

SOAP (Simple Object Access Protocol) o XML-RPC (XML Remote Procedure Call): Protocolos sobre los que se establece el intercambio; Este protocolo permite realizar intercambios de información entre diversas aplicaciones situadas en entornos que están descentralizados y se encuentran distribuidas. puede atravesar cortafuegos corporativos y ofrece la interoperabilidad de las aplicaciones. <br>

WSDL (Web Services Description Language): Es el estándar que se utiliza para describir un Servicio Web. Es una descripción basada en XML de los requisitos funcionales necesarios para establecer una comunicación con los servicios Web; permite especificar cómo deben representarse los parámetros, tanto de entrada como de salida, en una invocación de tipo externo al servicio. <br>

Web Services Protocol Stack: Así se denomina al conjunto de servicios y protocolos de los servicios Web. Otros protocolos: los datos en XML también pueden enviarse de una aplicación a otra mediante protocolos normales como HTTP (Hypertext Transfer Protocol), FTP (File Transfer Protocol), o SMTP (Simple UDDI (Universal Description, Discovery and Integration): Protocolo para publicar la información de los servicios Web. Permite comprobar qué servicios web están disponibles. WS-Security (Web Service Security): Protocolo de seguridad aceptado como estándar por OASIS. Garantiza la autenticación de los actores y la confidencialidad de los mensajes enviados. <br>

CC/PP: Capacidad de Composición/Perfiles de Preferencia (Composite Capability/Preference Profiles). Es un sistema basado en RDF, que permite estandarizar la forma de definir y transmitir información sobre las características de los dispositivos y las preferencias del usuario. <br>
                </font></p>

                





  
