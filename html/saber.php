                <p><font face="Arial" size="3" color = "black">La colocación de elementos para compartir en redes sociales en las páginas de un sitio web de comercio electrónico es una buena práctica. Esto se debe a que ayuda a generar presencia social y conversación sobre el producto en los medios sociales.

El uso plugins para compartir en redes sociales permite colocar botones de redes sociales en la página de productos. Estos botones, cuando los utiliza un visitante del sitio web, le permiten compartir la página del producto en la red social de su preferencia. Entre las muchas otras redes sociales que se muestran en el sitio web, la página del producto también se compartirá con éxito.

Facebook, Twitter e Instagram son algunas de las redes sociales mas usadas y reciben un gran compromiso de los usuarios. Aquí hay que entender que estas tres plataformas y otras similares como Pinterest tienen la configuración perfecta para compartir la página de producto.

