                <u><font face= "Arial" size="5">¿Cómo se consiguen?</font></u>
                <p><font face="Arial" size="3" color = "black"> Existen varias maneras de obtener la localización de un usuario de un sitio web, pero se pueden clasificar en dos en función de la intervención del usuario:

Obtención de la localización sin la intervención del usuario.
Obtención de la localización con la intervención del usuario.

La primera forma de obtención de la localización, de forma automática, tiene como ventaja que no hace intervenir al usuario, no se le hacen preguntas ni se requiere una participación activa. Debe tenerse en cuenta que la precisión de la localización del usuario obtenida de forma automática tiene sus limitaciones y puede no ser suficiente. <br>
La segunda forma, la determinación de la localización interviniendo el usuario, tiene como ventaja que éste obtiene exactamente lo que elige y que su elección no tienen por qué coincidir con regiones geográficas políticas concretas, sino que se puede ajustar a áreas geográficas arbitrarias.
<h2 class="title-section"><span class="marked">
                </span> SOA</h2>
                <u><font face="Arial" size="5">DIRECCIÓN IP</font></u>
                <p><font face="Arial" size="3" color = "black">La forma más habitual de obtención automática de la localización de un usuario de un sitio web es utilizar la dirección IP de la petición que realiza. Cuando un usuario está conectado a internet, su proveedor de acceso a internet le asigna un número denominado dirección IP. Este número lo recibe el sitio web y se utiliza para saber qué ruta debe seguir la información de respuesta para llegar hasta el ordenador del usuario que la ha solicitado. Los proveedores de acceso a internet tienen asignadas unas cuantas direcciones IP para que las utilicen sus clientes, y esas direcciones son públicas. Existen empresas que utilizan esa información junto con información de localización geográfica de las infraestructuras de los proveedores de acceso a internet para así poder determinar la localización de una dirección IP y vender esa información como servicio.
                La validez o no de los servicios de localización por dirección IP dependen del grado de precisión que se requiera. Conocer el país de origen es muy preciso, pero afinar más, intentando determinar la provincia o localidad es difícil o a veces imposible. A modo de ejemplo, en el sitio web de la empresa MaxMind se puede consultar la exactitud que ofrecen según el país y la precisión necesaria.

La falta de exactitud se debe a que las infraestructuras de red de los proveedores de alojamiento se concentran en lugares que ofrecen grandes velocidades de acceso a internet, y por tanto, por ejemplo, la mayoría de las veces que se solicita la localización de un usuario de internet en España se obtiene una localización de Madrid o Barcelona. Para paliar esto, algunas empresa de geolocalización realizan peticiones (técnicamente ‘ping’) desde ordenadores cuya localización exacta se conoce a la dirección IP que quieren localizar, y basándose en los tiempos de respuesta consiguen una mayor exactitud mediante triangulación.
                </font></p>
                <u><font face="Arial" size="5">NAVEGADOR</font></u>
                <p><font face="Arial" size="3" color = "black">W3C ha especificado una API de geolocalización para que los navegadores de internet puedan consultar a través de javascript la localización del equipo que está ejecutando el navegador web. Si se trata de un ordenador la localización estará basada en la dirección IP o red WiFi, pero si el navegador se está ejecutando en un dispositivo móvil y tiene GPS activado o se conecta a internet a través de una red de telefonía móvil, se podrá utilizar cualquier de estos elementos para poder obtener con más precisión la localización del usuario. Hay que destacar que el navegador solicitará permiso al usuario antes de obtener su localización y que éste puede negarse a darla.
                </font></p>
                <u><font face="Arial" size="5">USUARIO</font></u>
                <p><font face="Arial" size="3" color = "black">Normalmente la geolocalización no es el fin sino el medio para realizar ‘geotargeting’, y si la geolocalización no es lo suficientemente precisa, el sitio web deberá basarse en las respuestas de los usuarios para ofrecerles aquellos contenidos que se ajusten a sus demandas.

Pongamos por ejemplo un sitio web que ofrece información sobre actividades culturales en toda España. Si la geolocalización no es capaz de acertar con la provincia desde la que realiza la consulta un usuario, el sitio web podrá presentarle un selector de provincias para así mostrar aquella información local o cercana que pueda interesar al usuario. Debe tenerse en cuenta que esta selección de área geográfica deberá estar presente en cualquier caso, porque aunque se pueda determinar la localización del usuario correctamente por geolocalización, quizá éste quiere consultar información de otra provincia.
                </font></p>
                
