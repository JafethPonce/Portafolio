        <h2 class="title-section"><span class="marked">
                </span> SOA</h2>
                
                 <u><font face="Arial" size="5">Concepto:</font></u>
                 <p><font face="Arial" size="3" color = "black">La interfaz Geolocalización representa un objeto capaz de obtener mediante programación la posición del dispositivo. Se da acceso Web de la ubicación del dispositivo. <br>
                  Esto permite a un sitio Web o aplicación ofrecer resultados personalizados basados ​​en la ubicación del usuario.</font></p>
                <u><font face= "Arial" size="5">Beneficios de la geocalización: </font></u>
                <p><font face="Arial" size="3" color = "black">Beneficios: </font></p>
                <ul>
                    <li>
                    <font face="Arial" size="3" color = "black">Personalización de contenidos culturales o restricciones de derechos de autor según la localización del usuario..</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">Presentación de publicidad acorde a la localización del usuario, teniendo en cuenta el idioma, hora local, servicios y productos locales, fiestas locales, etc. Si el sitio web ofrece publicidad, ésta podrá elegirse con criterios más acertados, teniendo mayores posibilidades de éxito..</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">Prevención de fraudes, por ejemplo al realizar un pago con una tarjeta de crédito desde una localización no habitual..</font>
                    </li>
                    <li>
                    <font face="Arial" size="3" color = "black">Presentación de contenidos teniendo en cuenta la localización del usuario, su hora local, su idioma o su moneda. Esto aumenta la relevancia de la información que el sitio web presenta a los usuarios, mejorando la fidelización..</font>
